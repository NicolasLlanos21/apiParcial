package com.example.demo;

public class Values {
        
    private String name;
    private String date;
    private String autor;    
    private String cantidadNumeros;    
    private String sumaNumeros;    

    public String getCantidadNumeros() {
        return cantidadNumeros;
    }
    public void setCantidadNumeros(String cantidadNumeros) {
        this.cantidadNumeros = cantidadNumeros;
    }
    public String getSumaNumeros() {
        return sumaNumeros;
    }
    public void setSumaNumeros(String sumaNumeros) {
        this.sumaNumeros = sumaNumeros;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
    public String getAutor() {
        return autor;
    }
    public void setAutor(String autor) {
        this.autor = autor;
    }
    
}
